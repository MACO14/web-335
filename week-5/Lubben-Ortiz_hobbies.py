# Creates a list of days of the week 

days = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"]
for day in days: 
    if day == "Sunday":
        print("Congrats, it's the weekend and you don't have to work!")
    elif day == "Saturday":
        print("It's the weekend!")
    elif day == "Monday":
        print("It's Monday, a work day")   
    elif day == "Tuesday":
        print("It's Tuesday, a work day ")
    elif day == "Wednesday":
        print("It's Wednesday, a work day")
    elif day == "Thursday":
        print("It's Thursday, a work day")
    elif day == "Friday":
        print("It's Friday, a work day")
    else: 
        print(day)
    
    